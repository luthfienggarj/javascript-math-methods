/* Metode pow() menghitung pangkat suatu bilangan 
dengan menaikkan argumen kedua ke pangkat argumen pertama. */

// Contoh Pangkat Dengan Bilangan Positif Dan Pangkat Positif
let a = Math.pow(5, 2);
console.log(a);
// Output : 25

// Contoh Pangkat Dengan Bilangan Positif Dan Pangkat Negatif
let b = Math.pow(10, -2);
console.log(b);
// Output : 0.01

// Contoh Pangkat Dengan Bilangan Negatif Dan Pangkat Positf
let c = Math.pow(-2, 5);
console.log(c);
// Output : -32

// Contoh Pangkat Dengan Bilangan Negatif Dan Pangkat Negatif
let d = Math.pow(-4, -2);
console.log(d);
// Output : 0.0625

