/* Metode Math.exp() mengembalikan e (konstanta Euler) yang dipangkatkan. 
Ini setara dengan ex dalam matematika. */

// Normal Exp
// Hitung e dipangkatkan 1
let b = Math.exp(1);
console.log(b);
// Output : 2.718

// Hitung e dipangkatkan 2
let a = Math.exp(2);
console.log(a);
// Output : 7.389

// Hitung e dipangkatkan 3
let c = Math.exp(3);
console.log(c);
// Output : 20.085

// Hitung e dipangkatkan 4
let d = Math.exp(4);
console.log(d);
// Output : 54.598

// Angka 0
let e = Math.exp(0);
console.log(e);
// Output : 1

// Angka Negatif
let f = Math.exp(-1);
console.log(f);
// Output : 0.36